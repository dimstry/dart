// operator aritmatika adalah operator yang digunakan untuk melakukan operasi matematika
void main() {
  var result = 10 + 5; // penjumlahan
  print(result);
  var result2 = 10 - 5; // pengurangan
  print(result2);
  var result3 = 10 * 5; // perkalian
  print(result3);
  var result4 = 10 / 5; // pembagian
  print(result4);
  var result5 = 10 % 3; // modulus
  print(result5);
  var result6 = 10 ~/ 3; // pembagian bulat
  print(result6);
  var result7 = 10 + 5 * 2; // operator aritmatika berlaku dari kiri ke kanan
  print(result7);
  var result8 = (10 + 5) * 2; // operator aritmatika berlaku dari kiri ke kanan
  print(result8);
  var result9 =
      10 + 5 * 2 - 3; // operator aritmatika berlaku dari kiri ke kanan
  print(result9);
  var result10 =
      10 + 5 * (2 - 3); // operator aritmatika berlaku dari kiri ke kanan
  print(result10);
}
